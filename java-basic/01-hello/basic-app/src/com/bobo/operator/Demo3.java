package com.bobo.operator;

public class Demo3 {
    public static void main(String[] args) {
        // 目标：+ 符号连接符的识别
        int a = 6;
        System.out.println("abc" + 'a');
        System.out.println("abc" + a);
        System.out.println(5 + a);
        System.out.println(5 + 'a');
        System.out.println("" + a);
    }
}
