package com.bobo.operator;

public class Demo1 {
    public static void main(String[] args) {
        // 目标：掌握基本的算数运算符，+ - * / %
        int a = 10;
        int b = 3;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(a * 1.0 / b);
    }
}
