package com.bobo.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        // 变量：存储一个可变的值
        int age = 24;
        System.out.println(age);
        age = 25;
        System.out.println(age);

        double balance = 3.2;
        System.out.println(balance);
        balance += 3;
        System.out.println(balance);
    }
}
